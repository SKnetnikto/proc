export async function runCacheProbe() {
  const result = { error: null, wasmOk: false };

  const wasmBinary = new Uint8Array([0x00,0x61,0x73,0x6d,0x01,0x00,0x00,0x00,0x01,0x06,0x01,0x60,0x00,0x00,0x03,0x02,0x01,0x00,0x07,0x07,0x01,0x04,0x6d,0x61,0x69,0x6e,0x00,0x00,0x0a,0x04,0x01,0x02,0x00,0x0b]);

  try {
    const module = await WebAssembly.instantiate(wasmBinary);
    result.wasmOk = true;
    result.test = 'WASM loaded OK';
  } catch (e) {
    result.error = e.message || String(e);
  }

  return result;
}
